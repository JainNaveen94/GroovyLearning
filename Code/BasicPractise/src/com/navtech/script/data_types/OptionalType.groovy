package com.navtech.script.data_types

// optional Type Declaration
def optionalData

// Assign String to Optional Type
optionalData = "10"
println "Data Type : $optionalData.class"

// Assign Integer to Optional Type
optionalData = 10
println "Data Type : $optionalData.class"

// Assign Integer + String to Optional Type
optionalData = 10 + "10"
println "Data Type : $optionalData.class"

// Assign Boolean to Optional Type
optionalData = true
println "Data Type : $optionalData.class"

// Assign Long to Optional Type
optionalData = 123456789098
println "Data Type : $optionalData.class"

// Assign BigInteger to Optional Type
optionalData = 12345678909876543215775757
println "Data Type : $optionalData.class"

// Assign Double to Option Type
optionalData = 12.32d
println "Data Type : $optionalData.class"

// Assign Float to Optional Type
optionalData = 12.1f
println "Data Type : $optionalData.class"



