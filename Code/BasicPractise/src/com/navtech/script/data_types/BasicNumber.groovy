package com.navtech.script.data_types

// Integer Type which is not primitive instead Integer Wrapper Type
println 10.class

// Integer Type which is not primitive instead Integer Wrapper Type
int x = 10
println x.class

// BigInteger Type or BigLong
def a = 12345678900987654321
println a.class

// Long Type
def b = 1234567890123
println b.class

// BigDecimal Type
def c = 12.34
println c.getClass().getName()

// Double Type
def d = 12.34d
println d.class
